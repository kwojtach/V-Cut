/**************************
    NAVIGATION CONTROLLER 
***************************/
var mobileButton = document.querySelector('.navigation__mobile-icon');
var mobileIcon = document.querySelector('.navigation__mobile-icon i');
var mobileNav = document.querySelector('.navigation__mobile');

mobileButton.addEventListener('click', function(){

  // Change mobile icon based on current class
  if (mobileIcon.classList.contains('ion-navicon-round')) {
    mobileIcon.classList.add('ion-close-round');
    mobileIcon.classList.remove('ion-navicon-round');
    mobileNav.style.display = "block";

  } else {
    mobileIcon.classList.add('ion-navicon-round');
    mobileIcon.classList.remove('ion-close-round');
    mobileNav.style.display = "none";
  }

});